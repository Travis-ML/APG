def load_tools_from_file(path: str | Path) -> list[dict[str, Any]]:
    """Load MCP tools/list response from a JSON file.

    Accepts either the raw tools/list response:
      {"tools": [...]}

    Or a plain array of tool objects:
      [{"name": "read_file", ...}, ...]
    """
    path = Path(path)
    raw = json.loads(path.read_text(encoding="utf-8"))

    if isinstance(raw, list):
        return raw
    if isinstance(raw, dict) and "tools" in raw:
        return raw["tools"]
    raise ValueError(f"Unrecognized format in {path}. Expected {{'tools': [...]}} or [...].")


async def fetch_tools_from_gateway(
    gateway_url: str,
    mcp_path: str = "/mcp",
) -> list[dict[str, Any]]:
    """Fetch the federated tool list from AgentGateway's MCP endpoint.

    Sends a tools/list JSON-RPC request to AgentGateway and returns
    the tool definitions from the response.
    """
    import httpx

    jsonrpc_request = {
        "jsonrpc": "2.0",
        "id": 1,
        "method": "tools/list",
        "params": {},
    }

    async with httpx.AsyncClient() as client:
        resp = await client.post(
            f"{gateway_url}{mcp_path}",
            json=jsonrpc_request,
            headers={"Content-Type": "application/json"},
            timeout=30.0,
        )
        resp.raise_for_status()
        data = resp.json()

    result = data.get("result", {})
    tools = result.get("tools", [])

    if not tools:
        logger.warning(
            "No tools returned from %s%s. "
            "Ensure MCP servers are connected to AgentGateway.",
            gateway_url, mcp_path,
        )

    return tools
