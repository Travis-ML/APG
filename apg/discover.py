# YAML generation
# ---------------------------------------------------------------------------

def generate_mappings_yaml(
    discovered: list[DiscoveredMapping],
    include_comments: bool = True,
) -> str:
    """Generate tool_mappings.yaml content from discovered mappings.

    Includes provenance comments showing how each tool was classified
    and which ones need manual review.
    """
    lines: list[str] = []

    lines.append("# Tool Mappings - Auto-generated by APG discovery")
    lines.append("# Review all entries, especially those marked NEEDS REVIEW.")
    lines.append("#")

    # Summary
    high = [d for d in discovered if d.confidence == Confidence.HIGH]
    medium = [d for d in discovered if d.confidence == Confidence.MEDIUM]
    low = [d for d in discovered if d.confidence == Confidence.LOW]
    lines.append(f"# Discovered {len(discovered)} tools:")
    lines.append(f"#   {len(high)} high confidence (ToolAnnotations)")
    lines.append(f"#   {len(medium)} medium confidence (name heuristics)")
    lines.append(f"#   {len(low)} low confidence (needs review)")
    lines.append("")
    lines.append("mappings:")

    for d in sorted(discovered, key=lambda x: x.tool_name):
        lines.append("")

        if include_comments:
            lines.append(f"  # Confidence: {d.confidence.value}")
            lines.append(f"  # Reason: {d.classification_reason}")
            if d.description:
                desc = d.description[:120]
                lines.append(f"  # Description: {desc}")
            if d.needs_review:
                lines.append(f"  # *** NEEDS REVIEW: {d.review_reason} ***")

        lines.append(f"  {d.tool_name}:")
        lines.append(f"    action_type: {d.action_type}")

        if d.resource_field:
            lines.append(f"    resource_field: {d.resource_field}")
        else:
            lines.append(f"    resource_field: null")

        lines.append(f"    resource_prefix: \"{d.resource_prefix}\"")
        lines.append(f"    category: {d.category}")

        if d.risk_escalation:
            lines.append(f"    risk_escalation:")
            for rule in d.risk_escalation:
                lines.append(f"      - pattern: \"{rule['pattern']}\"")
                lines.append(f"        risk: {rule['risk']}")

    # Default section
    lines.append("")
    lines.append("# Default for unmapped tools")
    lines.append("default:")
    lines.append("  action_type: unknown")
    lines.append("  resource_field: null")
    lines.append("  resource_prefix: \"unknown:\"")
    lines.append("  category: unknown")
    lines.append("  risk_level: medium")
    lines.append("")

    return "\n".join(lines) + "\n"


